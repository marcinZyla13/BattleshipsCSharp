
namespace BattleShipsOOP
{
    internal class Coords
    {
        private int _x;
        private int _y;

        public Coords(int x ,int y )
        {
            _x=x;
            _y=y;
        }

        public int X()
        {
            return _x;
        }

        public int Y()
        {
            return _y;
        }


    }
}
